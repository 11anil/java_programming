package operators;

public class relational {
    public static void main(String args[]){

       // relational operators
        
       int  a = 10;
       int  b = 5;
    
     //equal to

      System.out.println(a==b);

      // not equal to

      System.out.println(a!=b);
      
      // greator than

      System.out.println(a>b);

      //less than

      System.out.println(a<b);

      //greator than - equal to

      System.out.println(a>=b);


    //   less than - equal to

      System.out.println(a<=b);

    }
}
